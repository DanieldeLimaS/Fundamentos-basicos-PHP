<?php

$valor = 20;

//Pré-incremento
echo "Pré-incremento<br>";
echo ++$valor;//retorna o valor com o incremento
echo "<hr>";
//Pós-incremento

echo "Pós-incremento<br>";
echo $valor++;//retorna o valor e depois ele incrementa
echo "<br>";
echo $valor;
echo "<hr>";

$valor=20;
//Pré-incremento
echo "Pré-decremento<br>";
echo --$valor;//retorna o valor com o incremento
echo "<hr>";
//Pós-incremento

echo "Pós-decremento<br>";
echo $valor--;//retorna o valor e depois ele incrementa
echo "<br>";
echo $valor;